package de.exxcellent.challenge.data;

public final class WeatherData {
	
	private int Day;
	private double MxT;
	private double MnT;
	
	public int getDay() {
		return Day;
	}
	public void setDay(int day) {
		Day = day;
	}
	public double getMxT() {
		return MxT;
	}
	public void setMxT(double mxT) {
		MxT = mxT;
	}
	public double getMnT() {
		return MnT;
	}
	public void setMnT(double mnT) {
		MnT = mnT;
	}

}
